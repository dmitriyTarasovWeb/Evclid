document.querySelector(".section-questions__button-1").onclick = function() {
    document.querySelector(".section-questions__dropdown-1").style.height=document.querySelector(".section-questions__dropdown-text-1").offsetHeight+"px"
    document.querySelector(".section-questions__button-1").classList.toggle("section-questions__button-change")
    document.querySelector(".section-questions__dropdown-1").classList.toggle("section-questions__dropdown-hide")
}

